     * Get an SVG icon by name
     * @param string $name Icon name
     * @param array $args Additional arguments (class, width, height, aria-label)
     * @return string SVG HTML
     */
    public static function get($name, $args = array()) {
        $defaults = array(
            'class' => '',
            'width' => 20,
            'height' => 20,
            'aria-label' => '',
            'aria-hidden' => true,
        );

        $args = wp_parse_args($args, $defaults);

        $class = 'rb-icon rb-icon--' . esc_attr($name);
        if (!empty($args['class'])) {
            $class .= ' ' . esc_attr($args['class']);
        }

        $aria_label = !empty($args['aria-label']) ? 'aria-label="' . esc_attr($args['aria-label']) . '"' : '';
        $aria_hidden = $args['aria-hidden'] ? 'aria-hidden="true"' : '';

        $svg_content = self::get_icon_path($name);

        if (empty($svg_content)) {
            return '';
        }

        return sprintf(
            '<svg class="%s" width="%d" height="%d" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg" %s %s>%s</svg>',
            $class,
            absint($args['width']),
            absint($args['height']),
            $aria_label,
            $aria_hidden,
            $svg_content
        );
    }

